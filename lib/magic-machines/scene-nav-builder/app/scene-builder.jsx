// ── Draggable scene element ───────────────────────────────────────────────
function SceneElement({ el, selected, preview, onSelect, onMove, containerRef }) {
  const [hovered, setHovered] = useState(false);
  const [dragging, setDragging] = useState(false);
  const start = useRef({ mx: 0, my: 0, ex: 0, ey: 0 });

  const onDown = useCallback((e) => {
    if (preview) return;
    e.preventDefault();
    e.stopPropagation();
    onSelect(el.id);
    setDragging(true);
    const rect = containerRef.current.getBoundingClientRect();
    start.current = { mx: e.clientX, my: e.clientY, ex: el.x, ey: el.y, rw: rect.width, rh: rect.height };
    const move = (ev) => {
      const dx = ((ev.clientX - start.current.mx) / start.current.rw) * 100;
      const dy = ((ev.clientY - start.current.my) / start.current.rh) * 100;
      onMove(el.id, start.current.ex + dx, start.current.ey + dy);
    };
    const up = () => { setDragging(false); window.removeEventListener("mousemove", move); window.removeEventListener("mouseup", up); };
    window.addEventListener("mousemove", move);
    window.addEventListener("mouseup", up);
  }, [el, preview, onSelect, onMove, containerRef]);

  const gI = el.glowIntensity || 1;
  const glowFilter = `drop-shadow(0 0 ${8*gI}px ${el.glowColor}) drop-shadow(0 0 ${20*gI}px ${el.glowColor}) drop-shadow(0 0 ${40*gI}px ${el.glowColor}80)`;

  return (
    <div
      onMouseDown={onDown}
      onMouseEnter={() => setHovered(true)}
      onMouseLeave={() => setHovered(false)}
      onClick={() => {
        if (preview && el.actionType === "link" && el.actionValue) window.open(el.actionValue, "_blank");
        if (preview && el.actionType === "anchor" && el.actionValue) {
          const t = document.getElementById(el.actionValue);
          if (t) t.scrollIntoView({ behavior: "smooth" });
        }
        if (preview && el.actionType === "filter") alert(`Filter → ${el.actionValue || "(no tag set)"}`);
      }}
      style={{
        position: "absolute",
        left: `${el.x}%`,
        top: `${el.y}%`,
        width: `${el.scale}%`,
        cursor: preview ? "pointer" : dragging ? "grabbing" : "grab",
        filter: hovered ? glowFilter : "none",
        transition: dragging ? "none" : "filter 0.35s ease, transform 0.25s ease",
        transform: hovered ? "scale(1.06)" : "scale(1)",
        zIndex: dragging ? 100 : selected ? 50 : 10,
        outline: !preview && selected ? `2px dashed ${el.glowColor}` : "none",
        outlineOffset: "4px",
      }}
    >
      <img src={el.src} alt={el.label || ""} draggable={false}
        style={{ width: "100%", height: "auto", display: "block", pointerEvents: "none", userSelect: "none" }} />
      {el.label && hovered && (
        <div style={{
          position: "absolute", bottom: "-28px", left: "50%", transform: "translateX(-50%)",
          background: "rgba(0,0,0,0.82)", color: "#fff", padding: "4px 12px", borderRadius: "14px",
          fontSize: "12px", whiteSpace: "nowrap", pointerEvents: "none",
          fontFamily: "'DM Sans', sans-serif", boxShadow: `0 0 12px ${el.glowColor}40`,
        }}>
          {el.label}
        </div>
      )}
    </div>
  );
}

// ── Inline text input ─────────────────────────────────────────────────────
function Field({ label, value, onChange, placeholder, mono }) {
  return (
    <div style={{ marginBottom: "10px" }}>
      <div style={{ fontSize: "10px", color: "#556", marginBottom: "3px", fontFamily: "'Space Mono', monospace", textTransform: "uppercase", letterSpacing: "0.08em" }}>{label}</div>
      <input value={value} onChange={e => onChange(e.target.value)} placeholder={placeholder}
        style={{
          width: "100%", padding: "7px 10px", borderRadius: "6px",
          border: "1px solid #1e1e30", background: "#0c0c18", color: "#ccc",
          fontSize: "13px", fontFamily: mono ? "'Space Mono', monospace" : "'DM Sans', sans-serif",
          outline: "none", boxSizing: "border-box", transition: "border-color 0.15s",
        }}
        onFocus={e => e.target.style.borderColor = "#444"}
        onBlur={e => e.target.style.borderColor = "#1e1e30"}
      />
    </div>
  );
}


