/**
 * Scene Nav Builder — embed HTML serializer.
 *
 * Replaces the former magic machine's separate embed generator by reusing the
 * shape's OWN renderer as the single source of truth: embed = render(scene)
 * + the shape's stylesheet text + a compact inline runtime (hover glow, click
 * actions incl. filter or/and mode).
 *
 * Pure given its inputs (cssText is passed in — the panel fetches
 * /assets/css/visualizers/scene-nav.css at export time; tests pass a string).
 */
import { render } from "../renderer.js";

/**
 * @param {object} sceneData - the scene data model (debug stripped)
 * @param {object} opts
 * @param {string} opts.cssText  - scene-nav stylesheet contents to inline
 * @param {string} [opts.baseUrl] - absolute origin to prefix relative img srcs (e.g. https://melt.bloob.haus)
 * @param {string} [opts.filterMode] - "or" | "and" (filter action behavior)
 * @returns {string} self-contained HTML snippet
 */
export function generateEmbedHtml(sceneData, { cssText, baseUrl = "", filterMode = "or" } = {}) {
  // Embeds never carry debug chrome
  const scene = { ...sceneData, debug: false };
  let html = render(scene);

  // Absolutize relative img srcs so the embed works on a foreign origin (Shopify)
  if (baseUrl) {
    const base = baseUrl.replace(/\/$/, "");
    html = html.replace(/src="(?!https?:\/\/|\/\/|data:)\/?([^"]+)"/g, (m, p) => `src="${base}/${p}"`);
  }

  const runtime = `(function(){
  var FILTER_MODE=${JSON.stringify(filterMode)};
  var active=[];
  document.querySelectorAll(".scene-nav-el").forEach(function(el){
    var glow=el.dataset.glow||"#FFD700";
    var k=Number(el.dataset.glowIntensity||1);
    var img=el.querySelector("img");
    el.addEventListener("mouseenter",function(){
      if(img)img.style.filter="drop-shadow(0 0 "+(6*k)+"px "+glow+") drop-shadow(0 0 "+(14*k)+"px "+glow+") drop-shadow(0 0 "+(28*k)+"px "+glow+")";
      if(el.dataset.resetRotationOnHover==="true")el.style.transform="rotate(0deg)";
    });
    el.addEventListener("mouseleave",function(){
      if(img)img.style.filter="";
      el.style.transform="rotate("+(el.dataset.rotation||0)+"deg)";
    });
    el.addEventListener("click",function(){
      var action=el.dataset.action,value=el.dataset.value;
      if(!value)return;
      if(action==="link"){window.open(value,"_blank");}
      else if(action==="anchor"){var t=document.getElementById(value);if(t)t.scrollIntoView({behavior:"smooth"});}
      else if(action==="filter"){
        var i=active.indexOf(value);
        if(i>=0)active.splice(i,1);else active.push(value);
        document.querySelectorAll("[data-filter-tags]").forEach(function(item){
          var tags=(item.dataset.filterTags||"").split(",");
          var show=active.length===0||(FILTER_MODE==="and"
            ? active.every(function(f){return tags.indexOf(f)>=0;})
            : active.some(function(f){return tags.indexOf(f)>=0;}));
          item.style.display=show?"":"none";
        });
      }
    });
  });
})();`;

  return `<!-- scene-nav embed (generated by bloob-haus scene-nav builder) -->
<style>
${cssText || ""}
</style>
${html}
<script>
${runtime}
</script>`;
}
