// ── Generate ```scene-nav``` YAML for markdown ────────────────────────────
function generateCodeFence(scene) {
  let yaml = "";
  if (scene.background) yaml += `background: ${scene.backgroundName || "background.png"}\n`;
  if (scene.elements.length === 0) return "```scene-nav\n" + yaml + "```";
  
  yaml += "elements:\n";
  scene.elements.forEach(el => {
    yaml += `  - image: ${el.fileName}\n`;
    yaml += `    x: ${Math.round(el.x * 10) / 10}\n`;
    yaml += `    y: ${Math.round(el.y * 10) / 10}\n`;
    yaml += `    scale: ${Math.round(el.scale * 10) / 10}\n`;
    if (el.label) yaml += `    label: "${el.label}"\n`;
    yaml += `    glow: "${el.glowColor}"\n`;
    if (el.glowIntensity !== 1) yaml += `    glowIntensity: ${el.glowIntensity}\n`;
    yaml += `    action: ${el.actionType}\n`;
    if (el.actionValue) yaml += `    value: "${el.actionValue}"\n`;
  });

  return "```scene-nav\n" + yaml + "```";
}

// ── Generate standalone embed HTML/CSS/JS ─────────────────────────────────
